    private void btn_actualizar_editarActionPerformed(java.awt.event.ActionEvent evt) {
        String nombre = txt_nombres_editar.getText();
        String apellidos = txt_apellidos_editar.getText();
        String dni = txt_dni_editar.getText();
        String email = txt_email_editar.getText();
        String genero = rb_m_editar.isSelected() ? "M" : rb_f_editar.isSelected() ? "F" : "";

        Cliente c = new Cliente();
        c.setIdcliente(this.idcliente); // se usa la variable interna
        c.setNombre(nombre);
        c.setApellidos(apellidos);
        c.setDni(dni);
        c.setEmail(email);
        c.setGenero(genero);

        IClienteDao cDAO = new ClienteDaoImpl();
        boolean actualizado = cDAO.actualizar(c);

        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Cliente actualizado correctamente.");
            this.dispose(); // cerrar ventana si quieres
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar el cliente.");
        }

    }//GEN-LAST:event_btn_actualizar_editarActionPerformed


