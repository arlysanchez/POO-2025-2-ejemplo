    public void BuscarCliente(int idcliente) {

        IClienteDao cDAO = new ClienteDaoImpl();
        Cliente c = cDAO.buscarPorId(idcliente);
        try {
            txt_nombres_editar.setText(c.getNombre());
            txt_apellidos_editar.setText(c.getApellidos());
            txt_dni_editar.setText(c.getDni());
            txt_email_editar.setText(c.getEmail());
       // Seleccionar el radio button correcto según el género
        if ("M".equalsIgnoreCase(c.getGenero())) {
            rb_m_editar.setSelected(true);
        } else if ("F".equalsIgnoreCase(c.getGenero())) {
            rb_f_editar.setSelected(true);
        } else {
            genero_editar.clearSelection();
        }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al cargar los datos.", "Error", JOptionPane.ERROR_MESSAGE);

        }

    }
    

