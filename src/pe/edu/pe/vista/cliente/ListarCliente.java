    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {
          if (tbl_clientes.getRowCount() > 0) { //tabla no vacia
            if (tbl_clientes.getSelectedRow() != -1) {
                int idcliente = Integer.parseInt(tbl_clientes.getValueAt(tbl_clientes.getSelectedRow(), 0).toString());
                System.out.println("id_cliente: "+idcliente);
                EditarCliente ec = new EditarCliente(idcliente);
                ec.setSize(700, 500);
                ec.setLocation(0, 0);
                ec.setVisible(true);
                ec.setLocationRelativeTo(null);
            }
        }
        
    }

