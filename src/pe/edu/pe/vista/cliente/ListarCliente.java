    public void cargarClientes() {
        List<Cliente> lista = CDao.listarClientes();

        DefaultTableModel tableModel = (DefaultTableModel) tbl_clientes.getModel();
        tableModel.setNumRows(0);
        for (Cliente c : lista) {
            String genero;
            if (c.getGenero().equals("M")) {
                genero = "Masculino";
            } else {
                genero = "Femenino";
            }
            Object[] rowData
                    = {
                        c.getIdcliente(),
                        c.getNombre(),
                        c.getApellidos(),
                        c.getDni(),
                        c.getEmail(),
                        genero
                    };
            tableModel.addRow(rowData);
        }
        tbl_clientes.setModel(tableModel);
        JOptionPane.showMessageDialog(null, "Carga Completada :) ", "INFORMATION_MESSAGE", JOptionPane.INFORMATION_MESSAGE);

    }

