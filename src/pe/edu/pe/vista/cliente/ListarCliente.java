    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {

     if (tbl_clientes.getRowCount() > 0) { //tabla no vacia
            if (tbl_clientes.getSelectedRow() != -1) {
                int idCliente = Integer.parseInt(tbl_clientes.getValueAt(tbl_clientes.getSelectedRow(), 0).toString());
                int resp = JOptionPane.showConfirmDialog(null, "Esta usted seguro de eliminar el cliente?");
                if (JOptionPane.OK_OPTION == resp) {
                    c.setIdcliente(idCliente);
                    CDao.eliminar(c);
                    JOptionPane.showMessageDialog(null, "usuario eliminado :( ", "INFORMATION_MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                    cargarClientes();
                } else {
                    JOptionPane.showMessageDialog(null, "Operación Cancelada :(", "INFORMATION_MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }

